package chap02.sec01.exam01_variable;

public class LiteralExample {
	public static void main(String[] args) {
		// 변수 선언
		// 자료형 변수명 = 리터럴
		int var1 = 10;
		
		// 표준 입출력(콘솔)에서 var1 변수값을 출력한다.
		System.out.println(var1);
		
		int var2 = 010; // 8진수
		System.out.println(var2);
		
		int var3 = 0x10; // 16진수ㄴ
		System.out.println(var3);
	}
}
